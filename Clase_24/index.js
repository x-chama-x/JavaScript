// Ordenando un arreglo en JS

var platillos = ["Pizza","Asado","Empanadas"];
console.log("antes ",platillos);
platillos.sort(); // ordena al arreglo de forma alfabetica
console.log("ordenado ",platillos);
platillos.reverse(); // invierte el orden de los elementos de un arreglo
console.log("despues ",platillos);


